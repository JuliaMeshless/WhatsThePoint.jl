function _discretize_volume(
        _cloud::PointCloud{𝔼{3}, C},
        spacing::AbstractSpacing,
        alg::Octree;
        max_points::Union{Int, Nothing} = nothing,
    ) where {C}
    isnothing(alg.triangle_octree.leaf_classification) &&
        throw(
        ArgumentError(
            "TriangleOctree must be built with classify_leaves=true. " *
                "Rebuild with: TriangleOctree(mesh; classify_leaves=true)"
        )
    )

    T = Float64

    # Build and classify node octree (using infrastructure from src/octree/spacing_criterion.jl)
    print("  Building node octree...")
    t1 = @elapsed node_tree = build_node_octree(alg.triangle_octree, spacing, alg.alpha, alg.node_min_ratio)
    n_leaves = length(collect(all_leaves(node_tree)))
    println(" done ($(n_leaves) leaves, $(round(t1, digits = 2))s)")

    print("  Classifying node octree leaves...")
    t2 = @elapsed classification = classify_node_octree(node_tree, alg.triangle_octree)
    println(" done ($(round(t2, digits = 2))s)")

    # Resolve the automatic point-count cap: when the caller leaves max_points
    # unset, estimate it from the spacing integral (∑ box_volume/h³).
    if isnothing(max_points)
        max_points = _estimate_volume_points(node_tree, classification, spacing)
        println("  Auto-estimated max_points = $max_points")
    end

    # :bridson is a single global pass — no per-leaf allocation, candidate
    # filtering, or deficit fill (each would break the separation guarantee).
    if alg.placement == :bridson
        seeds = [_extract_vertex(T, pt) for pt in points(boundary(_cloud))]
        print("  Bridson sampling ($(length(seeds)) boundary seeds)...")
        t3 = @elapsed vol_points = _generate_bridson(
            node_tree, classification, alg.triangle_octree, spacing, seeds, max_points;
            factor = alg.bridson_factor,
        )
        println(" done ($(length(vol_points)) points, $(round(t3, digits = 2))s)")
        return PointVolume([Point(pt...) for pt in vol_points])
    end

    print("  Collecting weighted leaves...")
    t3 = @elapsed interior, near_surface = _collect_weighted_leaves(node_tree, classification, spacing)
    println(" done (interior: $(length(interior.indices)), near_surface: $(length(near_surface.indices)), $(round(t3, digits = 2))s)")

    # Allocate points proportionally to weighted volumes
    interior_total_weight = sum(interior.weights; init = zero(T))
    near_surface_total_weight = sum(near_surface.weights; init = zero(T))
    total_weight = interior_total_weight + near_surface_total_weight
    total_weight <= zero(T) && return PointVolume(Point{𝔼{3}, C}[])

    interior_ratio = interior_total_weight / total_weight
    n_interior = round(Int, max_points * interior_ratio)
    n_near_surface = max_points - n_interior
    println("  Allocating $n_interior interior + $n_near_surface near-surface points")

    # Generate points. Interior leaves are trusted after classify_node_octree
    # demotes any leaf overlapping a triangle-octree boundary leaf, so samples
    # drawn from LEAF_INTERIOR (and the deficit fill, which pulls from the same
    # pool) do not need a per-point isinside check.
    raw_points = SVector{3, T}[]
    sizehint!(raw_points, max_points)

    print("  Generating interior points...")
    t4 = @elapsed interior_points = _generate_from_leaves(interior, node_tree, n_interior, alg.placement)
    println(" done ($(length(interior_points)) points, $(round(t4, digits = 2))s)")
    append!(raw_points, interior_points)

    n_candidates = round(Int, n_near_surface * alg.boundary_oversampling)
    print("  Generating near-surface candidates...")
    t5 = @elapsed candidates = _generate_from_leaves(near_surface, node_tree, n_candidates, alg.placement)
    println(" done ($(length(candidates)) candidates, $(round(t5, digits = 2))s)")

    print("  Filtering candidates (isinside check)...")
    t6 = @elapsed begin
        inside_mask = isinside(candidates, alg.triangle_octree)
        target = n_interior + n_near_surface
        for i in eachindex(candidates)
            length(raw_points) >= target && break
            inside_mask[i] && push!(raw_points, candidates[i])
        end
    end
    n_accepted = length(raw_points) - length(interior_points)
    println(" done ($n_accepted accepted, $(round(t6, digits = 2))s)")

    # Fill deficit from interior leaves. No isinside needed — interior leaves
    # are guaranteed not to overlap any boundary triangle-octree leaf.
    if length(raw_points) < max_points
        deficit = max_points - length(raw_points)
        println("  Filling deficit of $deficit points...")
        cumulative_weights = isempty(interior.weights) ? T[] : cumsum(interior.weights)
        total_weight = isempty(cumulative_weights) ? zero(T) : cumulative_weights[end]

        if total_weight > zero(T)
            for _ in 1:deficit
                idx = clamp(searchsortedfirst(cumulative_weights, rand(T) * total_weight), 1, length(interior.indices))
                bbox_min, bbox_max = box_bounds(node_tree, interior.indices[idx])
                push!(raw_points, _rand_point_in_box(bbox_min, bbox_max))
            end
        end
    end

    # Convert to Point objects
    return PointVolume([Point(pt...) for pt in raw_points])
end

"""
    _ensure_float64_boundary(bnd::PointBoundary) -> PointBoundary

Promote a boundary with a non-Float64 machine type to Float64, preserving
surface names, normals, and areas. Binary STL stores Float32 by spec, while
the Octree algorithm generates Float64 volume points and `PointCloud`
requires one CRS across boundary and volume — without promotion the cloud
assembly fails. Returns `bnd` unchanged when already Float64.
"""
function _ensure_float64_boundary(bnd::PointBoundary{M, C}) where {M, C}
    CoordRefSystems.mactype(C) === Float64 && return bnd
    promoted = [
        name => PointSurface(
            [Point((1.0 .* to(p))...) for p in points(surf)],
            collect(normal(surf)),
            [1.0 * a for a in area(surf)],
        ) for (name, surf) in namedsurfaces(bnd)
    ]
    isempty(promoted) && return bnd
    surfaces = LittleDict{Symbol, typeof(last(first(promoted)))}(promoted...)
    return PointBoundary(surfaces)
end
