"""
    Octree <: AbstractNodeGenerationAlgorithm

Spacing-driven volume discretization algorithm.

**Note:** This is not solution-adaptive (AMR). Refinement is determined a priori
by a prescribed spacing function, not by computed solution features.

!!! note
    `Octree` is a **discretization algorithm** that generates volume fill points.
    [`TriangleOctree`](@ref) is a separate **spatial data structure** used internally
    for mesh geometry queries. They serve different purposes.

# Algorithm
Uses dual octrees internally:
- **Triangle octree**: Captures geometry (surfaces, curvature)
- **Node octree**: Spacing-driven subdivision where `h_box ≤ alpha * h_spacing(x)`

Points are generated by weighted allocation across octree leaves based on
local spacing requirements.

# Key Parameters
- `min_ratio`: Triangle octree resolution (default: auto from mesh complexity)
- `node_min_ratio`: Node octree resolution (default: auto from `spacing` if provided)
- `alpha`: Subdivision aggressiveness, `h_box ≤ alpha * h_spacing` (default: 2.0, use 1.0 for fine boundary layers)
- `placement`: `:random`, `:jittered`, `:lattice`, or `:bridson` (default: `:random`; `:bridson` recommended for production)
- `bridson_factor`: Poisson-disk radius relative to `h(x)` for `:bridson` (default: 0.75)
- `boundary_oversampling`: Oversampling near boundaries (default: 2.0)

# Placement modes
`:random`, `:jittered`, and `:lattice` sample each octree leaf independently
(point counts allocated by leaf volume / local spacing). `:bridson` instead runs
a single global advancing-front Poisson-disk pass (Bridson 2007, graded to
`h(x)`) seeded from the boundary points: every generated point keeps a distance
of at least `min(rᵢ, rⱼ)` with `r = bridson_factor · h(x)` from every other
point — including the boundary — *by construction*. The front saturates at the
disk-packing density, so `max_points` acts as a cap, not a target; a warning is
emitted if the cap truncates the front before saturation (which would leave
unfilled regions).

`bridson_factor` (default 0.75) sets the disk radius relative to the local
spacing. A saturated 3D dart-throwing front packs ≈ 0.39 points per `r³`
(measured on this implementation, k = 30 attempts), so matching the prescribed
density `1/h³` requires `r ≈ 0.73·h`; the 0.75 default keeps the saturated
count just below the prescribed budget. Use `1.0` for strict `d_NN ≥ h`
Poisson-disk sampling — that yields ~45% fewer points than `1/h³` and starts
`repel`'s spacing-equilibrium force in its attractive branch, which degrades
rather than polishes the seeding (measured on the cavity gate).

# Examples
```julia
# Automatic (recommended)
alg = Octree(mesh; spacing, alpha=1.0)
cloud = discretize(boundary, spacing; alg, max_points=100_000)

# Manual geometry resolution
alg = Octree(mesh; min_ratio=1e-3, spacing, alpha=1.0)
```
"""
struct Octree{M <: Manifold, C <: CRS, T <: Real} <: AbstractNodeGenerationAlgorithm
    triangle_octree::TriangleOctree{M, C, T}
    boundary_oversampling::Float64
    placement::Symbol
    alpha::Float64  # Subdivision aggressiveness: boxes are at most alpha*h_local
    node_min_ratio::T  # Node octree minimum box size ratio (can be much finer than triangle octree)
    bridson_factor::Float64  # Poisson-disk radius relative to h(x) (placement = :bridson only)
end

# Constructors
function Octree(
        triangle_octree::TriangleOctree{M, C, T};
        node_min_ratio::Union{Nothing, Real} = nothing,
        boundary_oversampling::Real = 2.0,
        placement::Symbol = :random,
        alpha::Real = 2.0,
        bridson_factor::Real = 0.75,
    ) where {M, C, T}
    boundary_oversampling > 0 || throw(ArgumentError("boundary_oversampling must be positive"))
    placement in (:random, :jittered, :lattice, :bridson) ||
        throw(ArgumentError("placement must be :random, :jittered, :lattice, or :bridson"))
    alpha > 0 || throw(ArgumentError("alpha must be positive"))
    bridson_factor > 0 || throw(ArgumentError("bridson_factor must be positive"))

    # Default: use triangle octree's min_ratio
    node_ratio = isnothing(node_min_ratio) ? triangle_octree.tree.min_ratio : T(node_min_ratio)

    return Octree{M, C, T}(
        triangle_octree,
        Float64(boundary_oversampling),
        placement,
        Float64(alpha),
        node_ratio,
        Float64(bridson_factor)
    )
end

function Octree(
        mesh::SimpleMesh{M, C};
        spacing::Union{Nothing, AbstractSpacing} = nothing,
        min_ratio::Union{Nothing, Real} = nothing,
        node_min_ratio::Union{Nothing, Real} = nothing,
        tolerance_relative::Real = 1.0e-6,
        boundary_oversampling::Real = 2.0,
        placement::Symbol = :random,
        alpha::Real = 2.0,
        bridson_factor::Real = 0.75,
        verify_orientation::Bool = true,
    ) where {M, C}
    placement in (:random, :jittered, :lattice, :bridson) ||
        throw(ArgumentError("placement must be :random, :jittered, :lattice, or :bridson"))
    bridson_factor > 0 || throw(ArgumentError("bridson_factor must be positive"))
    T = Float64

    # Triangle octree: geometry-based or user override
    geometry_min_ratio = isnothing(min_ratio) ? _auto_min_ratio(T, mesh) : T(min_ratio)
    triangle_octree = TriangleOctree(
        mesh;
        tolerance_relative,
        min_ratio = geometry_min_ratio,
        classify_leaves = true,
        verify_orientation,
    )

    # Node octree: spacing-aware automatic or user override
    node_ratio = if !isnothing(node_min_ratio)
        # User provided explicit node_min_ratio
        T(node_min_ratio)
    elseif !isnothing(spacing)
        # Compute from spacing
        h_min = _extract_min_spacing(spacing)
        if !isnothing(h_min)
            # Compute domain size (strip units to get Float64)
            bbox = Meshes.boundingbox(mesh)
            extents = bbox.max - bbox.min
            # Get maximum extent value, stripping units
            extent_values = [T(ustrip(extents[i])) for i in 1:length(extents)]
            max_extent = maximum(extent_values)

            # Node octree must resolve features alpha times smaller than minimum spacing
            # to properly capture spacing variations across the domain
            min_resolvable_size = h_min / T(alpha)
            T(min_resolvable_size) / max_extent
        else
            # Unknown spacing type, fall back to triangle octree ratio
            geometry_min_ratio
        end
    else
        # No spacing hint, use triangle octree ratio
        geometry_min_ratio
    end

    return Octree{M, C, T}(
        triangle_octree,
        Float64(boundary_oversampling),
        placement,
        Float64(alpha),
        node_ratio,
        Float64(bridson_factor)
    )
end

Octree(filepath::String; kwargs...) = Octree(GeoIO.load(filepath).geometry; kwargs...)

